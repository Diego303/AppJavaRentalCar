    private void jTextFieldMatriculaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldMatriculaKeyTyped
        // TODO add your handling code here:
        char car = evt.getKeyChar();
        String texto2;
        
        //TRAZA
        System.out.printf("He tecleado %c en Num2 %n",car);
        
        if(!Character.isLetter(car) && !Character.isDigit(car)){ //Si no es digito, ignoralo
            evt.consume(); //Ignora el evento
            texto2 = jTextFieldMatricula.getText();
        } else {
            texto2 = jTextFieldMatricula.getText() + car;
        }        
        
        
        if(!texto2.isEmpty() && !jTextFieldMarca.getText().isEmpty() && !jTextFieldModelo.getText().isEmpty()){
            jButtonAñadirVOk.setEnabled(true);
        } else {
            jButtonAñadirVOk.setEnabled(false);
        }
    }//GEN-LAST:event_jTextFieldMatriculaKeyTyped

