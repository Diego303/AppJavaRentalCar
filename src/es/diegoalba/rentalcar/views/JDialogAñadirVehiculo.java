    private String abreDialogo(){
        JFileChooser dlgFileChooser = new JFileChooser();
        
        // ...solo para ficheros JFileChhoser.FILES_ONLY
        // ...solo para ficheros JFileChhoser.DIRECTORIES_ONLY
        // ...solo para ficheros JFileChhoser.FILES_AND_DIRECTORIES
        dlgFileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        
        //...solo imagenes
        FileNameExtensionFilter filtroImagen = 
                    new FileNameExtensionFilter("Archivos de imagen","jpg","png","jpeg"); 
        
        //....solo musica
        FileNameExtensionFilter filtroMusica = 
                    new FileNameExtensionFilter("Archivos de audio","mp4","mp3");
        
        dlgFileChooser.addChoosableFileFilter(filtroImagen);
        //dlgFileChooser.addChoosableFileFilter(filtroMusica);
        
        //...eliminar "todos los archivos"
        dlgFileChooser.setAcceptAllFileFilterUsed(true);
        
        //....NO mostrar ficheros ocultos
        dlgFileChooser.setFileHidingEnabled(false);
        
        dlgFileChooser.setCurrentDirectory(new File("D:/"));
        
        //Muestro el dialogo
        int ret = dlgFileChooser.showOpenDialog(this);
        
        //El dialogo se cerro, ¿que sucedio?
        if(ret != JFileChooser.APPROVE_OPTION){
            //NO ha hecho click en OK/yes
            return null;
        }
        
        //Hizo click en Aceptar/ok/yes
        File fichero = dlgFileChooser.getSelectedFile();
        
        //Devuelvo el nombre con la ruta absoluta
        return fichero.getAbsolutePath();
    }
    
    /**
     * Metodo que escala la imagen a la etiqueta.
     * @param ruta La imagen.
     * @param label LA etiqueta a la que se adaptara.
     */
    public void escalarImg(String ruta, JLabel label){
        ImageIcon img = new ImageIcon(ruta);
        Icon icono = new ImageIcon(
                   img.getImage().getScaledInstance(
                           jLabelFotoCoche.getWidth(),jLabelFotoCoche.getHeight(),1));
        
        label.setIcon(icono);
        this.repaint();
    }
        
